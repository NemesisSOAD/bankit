<h3>Premier lancement</h3>

<p>Lors du premier lancement de BankIt, la fenêtre suivante va apparaître&nbsp;:</p>
<div class="thumbnail screenshot"><img src="static/img/use/bankit-first-run.png"></div>
<p>Indiquer alors le solde de votre compte courant à la date d'aujourd'hui.
Ceci est très important afin que la synchronisation fonctionne correctement par la suite</p>

<p>Une fois le montant saisi, vous allez arriver sur la page principale de BankIt&nbsp;:</p>
<div class="thumbnail screenshot"><img src="static/img/use/bankit-operation-view.png"></div>
<p>La vue par défaut affiche la liste des opérations de votre compte.
A partir de là, vous pouvez&nbsp;:</p>
<ul>
	<li>Ajouter des charges et des revenus récurents : <a href="?p=use.htm&s=costs.htm">Charges/Revenus</a></li>
	<li>Ajouter des opérations planifiées : <a href="?p=use.htm&s=operations.htm">Opérations</a></li>
</ul>
<p>La <a href="?p=use.htm&s=sync.htm">synchronisation</a> à partir de la banque ne peux s'effectuer qu'a partir du lendemain.</p>